def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    writer.undo()
    writer.write(state['score'])

    clear()

    # Move Pac-Man
    if valid(pacman + aim):
        pacman.move(aim)

    # Check for collision with food tile
    index = offset(pacman)
    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    # Draw Pac-Man
    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    # Move ghosts
    for point, course in ghosts:
        # Calculate the best direction for the ghost to move towards Pac-Man
        options = [
            vector(2.5, 0),
            vector(-2.5, 0),
            vector(0, 2.5),
            vector(0, -2.5),
        ]
        best_option = None
        min_distance = float('inf')

        for option in options:
            new_point = point + option
            if valid(new_point):
                distance_to_pacman = abs(new_point - pacman)
                if distance_to_pacman < min_distance:
                    min_distance = distance_to_pacman
                    best_option = option

        # Update the ghost's course
        if best_option is not None:
            course.x = best_option.x
            course.y = best_option.y

        # Move the ghost if the new position is valid
        if valid(point + course):
            point.move(course)

        # Draw the ghost
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    # Check for collision between Pac-Man and ghosts
    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    # Schedule the next move
    ontimer(move, 50)



def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


